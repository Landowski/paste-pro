// Function to detect user language
function detectLanguage() {
  const userLang = navigator.language || navigator.userLanguage;
  
  // Handle specific language codes
  if (userLang.startsWith('pt-BR') || userLang.startsWith('pt_BR')) {
    return 'pt_BR';
  }
  
  // Get the base language code
  const langCode = userLang.split('-')[0];
  
  // Check if we have translation for this language
  if (translations[langCode]) {
    return langCode;
  }
  
  // Default to English
  return 'en';
}

// Function to check if device is mobile
function isMobileDevice() {
  return window.innerWidth <= 768 || /Android|webOS|iPhone|iPad|iPod|BlackBerry|IEMobile|Opera Mini/i.test(navigator.userAgent);
}

// Function to translate the page
function translatePage(lang) {
  const translation = translations[lang];
  
  if (!translation) return;
  
  // Update all translatable elements
  Object.keys(translation).forEach(key => {
    const element = document.getElementById(key);
    if (element) {
      element.textContent = translation[key];
    }
  });
}

// Function to show mobile-only content
function showMobileContent() {
  const desktopContent = document.getElementById('desktopContent');
  const mobileContent = document.getElementById('mobileContent');
  
  if (desktopContent && mobileContent) {
    desktopContent.style.display = 'none';
    mobileContent.style.display = 'flex';
  }
}

// Function to show desktop content
function showDesktopContent() {
  const desktopContent = document.getElementById('desktopContent');
  const mobileContent = document.getElementById('mobileContent');
  
  if (desktopContent && mobileContent) {
    desktopContent.style.display = 'flex';
    mobileContent.style.display = 'none';
  }
}

// Function to handle responsive behavior
function handleResponsive() {
  if (isMobileDevice()) {
    showMobileContent();
  } else {
    showDesktopContent();
  }
}

function redirectToChromeWebStore() {
  window.open('https://chromewebstore.google.com/detail/paste-pro-text-snippets/pnhlkmibmkgbccponpdmbehbhlnapkme', '_blank');
}

function setupInstallButtons() {
  const installButtons = [
    document.getElementById('install'),
    document.getElementById('getItNow'),
    document.getElementById('getStarted')
  ].filter(button => button !== null);
  
  installButtons.forEach(button => {
    button.addEventListener('click', redirectToChromeWebStore);
    
    if (button.id === 'install' || button.id === 'getItNow' || button.id === 'getStarted') {
      const parentButton = button.closest('button');
      if (parentButton && !parentButton.hasListener) {
        parentButton.addEventListener('click', redirectToChromeWebStore);
        parentButton.hasListener = true;
      }
    }
  });
}

// Initialize the page
function init() {
  const userLang = detectLanguage();
  translatePage(userLang);
  handleResponsive();
  setupInstallButtons(); 
}

// Event listeners
document.addEventListener('DOMContentLoaded', init);
window.addEventListener('resize', handleResponsive);

